public class Relational{
    public static void main(String[] args) {
        int var1=5;
        int var2=484848468;
        System.out.println(var1>var2); //false
        System.out.println(var1==var2);  //false
        System.out.println(var1<var2);// true
        System.out.println(var1>=var2);  //false
        System.out.println(var1<=var2); //true
        System.out.println(var1!=var2); // true


    }
}