public class Unary{
    public static void main(String[] args) {
        int num=10;
        int num1=5;
        System.out.println(num++);
        System.out.println(num);
        System.out.println(++num);
        System.out.println(num--);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(!(num==num1));
    }
}
